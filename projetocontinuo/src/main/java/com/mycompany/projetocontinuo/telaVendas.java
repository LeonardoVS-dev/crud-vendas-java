    private void consultaClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultaClienteActionPerformed
        conexaoDB.Conectar();
        String sql;
        modeloCliente.setRowCount(0); 
        try {
            sql = "select * from clientes where nome like '%" + campoCliente.getText() + "%'"; 
            PreparedStatement stmt = con.prepareStatement(sql, ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
            ResultSet rs = stmt.executeQuery(sql); 
            rs.first();  
            String[] linha = new String[]{rs.getString("idCliente"),rs.getString("nome"),rs.getString("email"),rs.getString("telefone"),rs.getString("cpf"),rs.getString("cep")};
            modeloCliente.addRow(linha);
            while(rs.next()){
                linha = new String[]{rs.getString("idCliente"),rs.getString("nome"),rs.getString("email"),rs.getString("telefone"),rs.getString("cpf"),rs.getString("cep")};
                modeloCliente.addRow(linha);
            }
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Erro ao consultar, tente novamente","ERRO",JOptionPane.INFORMATION_MESSAGE);
            System.out.println(e);
        }
    }//GEN-LAST:event_consultaClienteActionPerformed

