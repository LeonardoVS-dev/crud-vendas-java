    private void consultaTransportadoraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultaTransportadoraActionPerformed
        conexaoDB.Conectar();
        String sql;
        modeloTransportadora.setRowCount(0); 
        try {
            sql = "select * from transportadores where nome like '%" + campoTRansportadora.getText() + "%'"; 
            PreparedStatement stmt = con.prepareStatement(sql, ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
            ResultSet rs = stmt.executeQuery(sql); 
            rs.first();  
            String[] linha = new String[]{rs.getString("idTransportador"),rs.getString("nome"),rs.getString("email"),rs.getString("telefone"),rs.getString("cnpj"),rs.getString("cep")};
            modeloTransportadora.addRow(linha);
            while(rs.next()){
                linha = new String[]{rs.getString("idProduto"),rs.getString("nome"),rs.getString("descricao")};
                modeloTransportadora.addRow(linha);
            }
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Erro ao consultar, tente novamente","ERRO",JOptionPane.INFORMATION_MESSAGE);
            System.out.println(e);
        }
    }//GEN-LAST:event_consultaTransportadoraActionPerformed

